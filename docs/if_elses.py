# 기본 if 구문
if True : 
    pass

# 기본 if - else 구문
if True : 
    pass
else :
    pass

# 기본 if - elif - else 구문
if True : 
    pass
elif True : 
    pass
else :
    pass

# 질문 형식(conditon,상태) = 변수 + 부등호 + 기준값
# 문자에 대한 긍정으로 질문
str_name = "youjin kim"
if str_name == "youjin kim" : 
    pass
    print("name is {}.".format(str_name))

# 문자에 대한 부정으로 질문
if str_name != "youjin kim" : 
    pass
    print("name is not {}.".format(str_name))

print("End Program!")
